    @Override
    protected void onPause() {
        super.onPause();
        SharedPreferences preferences = getSharedPreferences("STOREDVALUES", MODE_PRIVATE);
        SharedPreferences.Editor editor = preferences.edit();
        editor.putString("pakkaclass", noOfpakkaClassrooms.getText().toString());
        editor.putString("pakkaother", noOfpakkaOtherrooms.getText().toString());
        editor.putString("kachaclass", noOfkachaClassrooms.getText().toString());
        editor.putString("kachaothers", noOfkachaOtherrooms.getText().toString());
        editor.putString("pakkatot", pakka_total.getText().toString());
        editor.putString("kachatot", kacha_total.getText().toString());
        editor.putString("combineddot", combined_total.getText().toString());
        editor.apply();
    }

    @Override
    protected void onResume() {
        super.onResume();
        SharedPreferences preferences = getSharedPreferences("STOREDVALUES", MODE_PRIVATE);
        String covered = preferences.getString("pakkaclass", "");
        String uncovered = preferences.getString("pakkaother", "");
        String brooms = preferences.getString("kachaclass", "");
        String grooms = preferences.getString("kachaothers", "");
        String srooms = preferences.getString("pakkatot", "");
        String trooms = preferences.getString("kachatot", "");
        String SPINNER = preferences.getString("combineddot", "");
        noOfpakkaClassrooms.setText(covered);
        noOfpakkaOtherrooms.setText(uncovered);
        noOfkachaClassrooms.setText(brooms);
        noOfkachaOtherrooms.setText(grooms);
        pakka_total.setText(srooms);
        kacha_total.setText(trooms);
        combined_total.setText(SPINNER);

    }

    @Override
    public void onBackPressed() {
        AlertDialog.Builder builder1 = new AlertDialog.Builder(B_Construction.this);
        builder1.setMessage("Are you sure to go to roster screen?");
        builder1.setCancelable(false);

        builder1.setPositiveButton(
                "Yes",
                new DialogInterface.OnClickListener() {
                    public void onClick(DialogInterface dialog, int id) {
                        Intent intent = new Intent(getApplicationContext(), Roster_List.class);
                        intent.setFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP);
                        intent.putExtra("EXIT", true);
                        startActivity(intent);
                        overridePendingTransition(R.anim.fadein, R.anim.fadeout);
                    }
                });

        builder1.setNegativeButton(
                "Cancel",
                new DialogInterface.OnClickListener() {
                    public void onClick(DialogInterface dialog, int id) {
                        dialog.dismiss();
                    }
                });

        AlertDialog alert11 = builder1.create();
        alert11.show();
    }
